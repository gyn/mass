uint32_t _bitswap32(uint32_t value)
{
	uint8_t v1;
	uint8_t v2;
	uint8_t v3;
	uint8_t v4;

	uint8_t d[4];

	*(uint32_t *)d = value;

	v1 = d[0];
	v2 = d[1];
	v3 = d[2];
	v4 = d[3];

	d[0] = swap_table[v4];
	d[1] = swap_table[v3];
	d[2] = swap_table[v2];
	d[3] = swap_table[v1];

	return *(uint32_t *)d;
}

uint32_t bitswap32(uint32_t value)
{
	uint32_t temp;
	uint32_t result;

	temp = value;

	temp = ((temp & 0x55555555) << 1) | ((temp & 0xAAAAAAAA) >> 1);
	temp = ((temp & 0x33333333) << 2) | ((temp & 0xCCCCCCCC) >> 2);
	temp = ((temp & 0x0F0F0F0F) << 4) | ((temp & 0xF0F0F0F0) >> 4);
	temp = ((temp & 0x00FF00FF) << 8) | ((temp & 0xFF00FF00) >> 8);
	temp = ((temp & 0x0000FFFF) << 16) | ((temp & 0xFFFF0000) >> 16);

	result = temp;

	return result;
}

uint32_t bitswap8(uint8_t value)
{
	uint8_t temp;
	uint8_t result;

	temp = value;

	temp = ((temp & 0x55) << 1) | ((temp & 0xAA) >> 1);
	temp = ((temp & 0x33) << 2) | ((temp & 0xCC) >> 2);
	temp = ((temp & 0x0F) << 4) | ((temp & 0xF0) >> 4);

	result = temp;

	return result;
}

